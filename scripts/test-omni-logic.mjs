#!/usr/bin/env node
/**
 * Logic tests for Omni's pure, headless-testable helpers.
 *
 * Omni is a single self-contained HTML file, so rather than duplicate its logic
 * here (and risk drift), this test RUNS the page's real inline <script> in a
 * `vm` sandbox — the same technique as scripts/smoke-prototypes.mjs — and then
 * exercises the small `window.OmniCore` surface the app exposes for testing:
 * the calculator's expression evaluator, the unit/temperature converters, the
 * slugifier, and the from-scratch QR encoder.
 *
 * The QR check is a regression lock: the expected matrix below was generated by
 * this same encoder and independently verified bit-for-bit against the
 * `qrcode` npm reference library (4 strings × 4 ECC levels × 8 masks, byte
 * mode). If the encoder ever drifts, this fixture catches it — with no runtime
 * dependency. Zero deps — Node built-ins only. Run: `node scripts/test-omni-logic.mjs`.
 */
import { readFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import vm from 'node:vm';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');

/* ---- load the app's inline script and grab window.OmniCore ---- */
function makeStub() {
  const fn = function () { return stub; };
  const stub = new Proxy(fn, {
    get(_t, k) {
      if (k === Symbol.toPrimitive) return (h) => (h === 'string' ? '' : 0);
      if (k === 'toString') return () => '';
      if (k === 'valueOf') return () => 0;
      if (k === 'length') return 0;
      if (k === 'then') return undefined;
      if (k === Symbol.iterator) return function* () {};
      return stub;
    },
    set() { return true; }, apply() { return stub; }, construct() { return stub; }, has() { return true; },
  });
  return stub;
}
function loadOmniCore() {
  const html = readFileSync(join(ROOT, 'omni', 'index.html'), 'utf8');
  const m = html.match(/<script\b(?![^>]*\bsrc=)(?![^>]*type\s*=\s*["']?module)[^>]*>([\s\S]*?)<\/script>/i);
  if (!m) throw new Error('no inline script found in omni/index.html');
  const noop = () => {};
  const sb = {
    Math, Date, JSON, Object, Array, String, Number, Boolean, Symbol, RegExp, Map, Set,
    parseInt, parseFloat, isNaN, isFinite, Promise, URLSearchParams, console, Intl,
    encodeURIComponent, decodeURIComponent,
    setTimeout: () => 0, clearTimeout: noop, setInterval: () => 0, clearInterval: noop,
    requestAnimationFrame: () => 0, cancelAnimationFrame: noop, queueMicrotask: noop,
    performance: { now: () => Date.now() }, fetch: () => Promise.resolve(makeStub()),
    crypto: { getRandomValues: (a) => a, randomUUID: () => 'x' },
    document: makeStub(), navigator: makeStub(), location: makeStub(), history: makeStub(),
    localStorage: { getItem: () => null, setItem() {}, removeItem() {}, clear() {}, key: () => null, length: 0 },
    addEventListener: noop, removeEventListener: noop, matchMedia: () => makeStub(),
    getComputedStyle: () => makeStub(), scrollTo: noop, devicePixelRatio: 2, innerWidth: 390, innerHeight: 780,
  };
  sb.window = sb; sb.self = sb; sb.globalThis = sb;
  const ctx = vm.createContext(sb);
  vm.runInContext(m[1], ctx, { filename: 'omni/index.html#script', timeout: 5000 });
  if (!sb.OmniCore) throw new Error('window.OmniCore was not exposed');
  return sb.OmniCore;
}

/* ---- tiny assert harness ---- */
let pass = 0, fail = 0;
function ok(name, cond, detail) { if (cond) { pass++; console.log('  ✓ ' + name); } else { fail++; console.log('  ✗ ' + name + (detail ? '  — ' + detail : '')); } }
function near(a, b) { return Math.abs(a - b) < 1e-9; }

const C = loadOmniCore();

/* ---- calculator ---- */
console.log('calculator (expression evaluator)');
[['2+3*4', 14], ['12*(3+4)^2', 588], ['-5+3', -2], ['2^3^2', 512], ['sqrt(16)+1', 5],
 ['100%30', 10], ['(1+2)*(3+4)', 21], ['10/4', 2.5], ['sin(0)', 0], ['pi>3', null], ['abs(-7)', 7]]
.forEach(([e, exp]) => {
  if (exp === null) return; // skip placeholder
  let got; try { got = C.calcEval(e); } catch (err) { got = 'threw:' + err.message; }
  ok(e + ' = ' + exp, near(got, exp), 'got ' + got);
});
let threw = false; try { C.calcEval('2++'); } catch (e) { threw = true; }
ok('invalid expression throws', threw);

/* ---- converters ---- */
console.log('converters');
ok('1 km = 1000 m', near(C.convNonTemp('Length', 1, 'km', 'm'), 1000));
ok('1 mi ≈ 1.609 km', near(C.convNonTemp('Length', 1, 'mi', 'km'), 1.609344));
ok('1 kg = 1000 g', near(C.convNonTemp('Mass', 1, 'kg', 'g'), 1000));
ok('1024 B = 1 KB', near(C.convNonTemp('Data', 1024, 'B', 'KB'), 1));
ok('0°C = 32°F', near(C.convTemp(0, 'C', 'F'), 32));
ok('100°C = 212°F', near(C.convTemp(100, 'C', 'F'), 212));
ok('0°C = 273.15 K', near(C.convTemp(0, 'C', 'K'), 273.15));

/* ---- text ---- */
console.log('text helpers');
ok('slugify', C.slugify('  Hello, World!  ') === 'hello-world', 'got ' + C.slugify('  Hello, World!  '));
ok('titleCase', C.titleCase('the quick brown FOX') === 'The Quick Brown Fox');

/* ---- QR encoder (regression lock; verified vs qrcode@1.5.4 bit-for-bit) ---- */
console.log('QR encoder');
const EXPECTED = [
'1111111000110100101111111','1000001011110100101000001','1011101011000100101011101','1011101001010110001011101',
'1011101001111011101011101','1000001000000010101000001','1111111010101010101111111','0000000000111000000000000',
'0111011000101101100000110','1010010011101100100100101','0100101110111100000010100','0101010001011000010101110',
'0110111101100011111011010','0010010011011011101010110','0101011100101010010001110','1000010101100111000101010',
'0010001110110001111110101','0000000010100111100010100','1111111000100000101011011','1000001010010101100011101',
'1011101001111010111110000','1011101010001111111001011','1011101011000010011000110','1000001011100001010100000',
'1111111000100010011110011'];
const m = C.qrMatrix('OMNI ✅ everything', 'Q', 3);
ok('version 2 (25×25) size', m.length === 25, 'size ' + m.length);
const got = m.map((row) => row.map((c) => (c ? 1 : 0)).join(''));
let qrDiff = 0;
for (let i = 0; i < EXPECTED.length; i++) if (got[i] !== EXPECTED[i]) qrDiff++;
ok('matrix matches verified fixture', qrDiff === 0, qrDiff + ' rows differ');
// structural sanity: finder pattern present at all three corners
function finderOK(mm, oy, ox) {
  for (let y = 0; y < 7; y++) for (let x = 0; x < 7; x++) {
    const ring = (x === 0 || x === 6 || y === 0 || y === 6);
    const core = (x >= 2 && x <= 4 && y >= 2 && y <= 4);
    const want = ring || core;
    if (!!mm[oy + y][ox + x] !== want) return false;
  }
  return true;
}
const n = m.length;
ok('finder top-left', finderOK(m, 0, 0));
ok('finder top-right', finderOK(m, 0, n - 7));
ok('finder bottom-left', finderOK(m, n - 7, 0));
ok('auto-mask code is valid (no throw, right size)', C.qrMatrix('hello world', 'M').length === 21);

/* ---- summary ---- */
console.log('\n' + pass + ' passed, ' + fail + ' failed');
process.exit(fail > 0 ? 1 : 0);
